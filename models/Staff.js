const mongoose = require('mongoose');
const Schema = mongoose.Schema;

//Create Schema
const StaffSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});


module.exports = Staff = mongoose.model('staffs', StaffSchema);
